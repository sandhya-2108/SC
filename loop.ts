console.log("For Loop:");
for (let i: number = 1; i <= 5; i++) {
    console.log(i);
}

console.log("While Loop:");
let num: number = 1;
while (num <= 5) {
    console.log(num);
    num++;
}

console.log("Do-While Loop:");
let x: number = 1;
do {
    console.log(x);
    x++;
} while (x <= 5);